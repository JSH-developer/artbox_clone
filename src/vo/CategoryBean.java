package vo;

public class CategoryBean {
	private int category_num;
	private String category_code;
	private String category_sup;
	private String category_sub;
	public int getCategory_num() {
		return category_num;
	}
	public void setCategory_num(int category_num) {
		this.category_num = category_num;
	}
	public String getCategory_code() {
		return category_code;
	}
	public void setCategory_code(String category_code) {
		this.category_code = category_code;
	}
	public String getCategory_sup() {
		return category_sup;
	}
	public void setCategory_sup(String category_sup) {
		this.category_sup = category_sup;
	}
	public String getCategory_sub() {
		return category_sub;
	}
	public void setCategory_sub(String category_sub) {
		this.category_sub = category_sub;
	}
	
	

}
