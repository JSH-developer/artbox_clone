package vo;

public class QuestionBean {
	private int question_num;
	private String question_email;
	private String question_field;
	private String question_title;
	private String question_content;
	private String question_answer;
	private String question_member_id;
	private int question_product_num;
	
	public int getQuestion_num() {
		return question_num;
	}
	public void setQuestion_num(int question_num) {
		this.question_num = question_num;
	}
	public String getQuestion_email() {
		return question_email;
	}
	public void setQuestion_email(String question_email) {
		this.question_email = question_email;
	}
	public String getQuestion_field() {
		return question_field;
	}
	public void setQuestion_field(String question_field) {
		this.question_field = question_field;
	}
	public String getQuestion_title() {
		return question_title;
	}
	public void setQuestion_title(String question_title) {
		this.question_title = question_title;
	}
	public String getQuestion_content() {
		return question_content;
	}
	public void setQuestion_content(String question_content) {
		this.question_content = question_content;
	}
	public String getQuestion_answer() {
		return question_answer;
	}
	public void setQuestion_answer(String question_answer) {
		this.question_answer = question_answer;
	}
	public String getQuestion_member_id() {
		return question_member_id;
	}
	public void setQuestion_member_id(String question_member_id) {
		this.question_member_id = question_member_id;
	}
	public int getQuestion_product_num() {
		return question_product_num;
	}
	public void setQuestion_product_num(int question_product_num) {
		this.question_product_num = question_product_num;
	}
	
}
